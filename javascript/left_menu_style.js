//onclick에서 open_menu를 호출시 menu_slide class의 width를 200px로 변경
function open_menu() {
    document.getElementById("menu_slide").style.width = "200px";
}

//onclick에서 close_menu를 호출시 menu_slide class의 width를 0px로 변경
function close_menu() {
    document.getElementById("menu_slide").style.width = "0";
}